# Chapter 54: Man-Made Filaments; Strip and the Like of Man-Made Textile Materials

CHAPTER_54_NOTES = """
CHAPTER 54 - MAN-MADE FILAMENTS; STRIP AND THE LIKE OF MAN-MADE TEXTILE MATERIALS

Notes:
1. "Man-made fibers" throughout tariff schedule = staple fibers and filaments of organic polymers produced by manufacturing processes:
   (a) SYNTHETIC: polymerization of organic monomers producing polyamides, polyesters, polyolefins, polyurethanes, or chemical modification of such polymers (e.g., poly(vinyl alcohol) from hydrolysis of poly(vinyl acetate));
   (b) ARTIFICIAL: dissolution or chemical treatment of natural organic polymers (e.g., cellulose) producing cuprammonium rayon (cupro), viscose rayon, or chemical modification of natural organic polymers (cellulose, casein, proteins, alginic acid) producing cellulose acetate, alginates.

2. "Synthetic" = fibers defined in (a); "Artificial" = fibers defined in (b).

3. Strip and the like of heading 5404 or 5405 are NOT man-made fibers.

4. "Man-made", "synthetic", "artificial" have same meanings when applied to "textile materials".

5. Headings 5402 and 5403 do NOT apply to synthetic or artificial filament tow of chapter 55.

Statistical Notes - Woven fabric definitions:
(a) Unless context requires otherwise, provisions for one or more weaves embrace only fabrics (excluding selvage) wholly of specified weave(s).

(b) "Flat fabrics" = fabrics not containing yarns twisted >472 turns per meter.

(c) "Poplin or broadcloth" = plain weave, not square construction, whether or not napped. EXCLUDES: fabrics ≤170 g/m² with ≤33 warp ends and filling picks per cm²; fabrics >170 g/m² of average yarn number 26 or lower.

(d) "Sheeting" = plain weave, whether or not napped:
    (i) ≤170 g/m², square construction, >33 warp ends and filling picks per cm², average yarn number 68 or lower (excluding printcloth);
    (ii) >170 g/m², EXCLUDING: fabrics with multiple/cabled warp or filling of average yarn number 26 or lower; fabrics of average yarn number 27+ not of square construction.

(e) "Printcloth" = plain weave, ≤170 g/m², average yarn numbers 43-68, >33 singles yarns per cm², no multiple/cabled yarns, square construction, whether or not napped: (i) not combed; (ii) other fabrics <168 cm width.

(f) "Square construction": (i) <79 warp ends and filling picks per cm², difference <11; OR (ii) ≥79 warp ends and filling picks per cm², each count <57% of total.

(g) "Napped" = fuzzy, fibrous surface from scratching/pricking to raise fibers. NOT pile fabrics.

(h) "Not combed" = fabrics partly of uncombed cotton, other vegetable textile fibers, wool, or fine animal hair.

(ij) "Combed" = fabrics containing combed cotton, other vegetable textile fibers, wool, or fine animal hair.

(k) "Number" = average yarn number. Formula: N = BYT/1000, 100T/Z', BT/Z or ST/10. Disregard fractions.

"Discharge printed" = fabrics: (a) dyed single uniform color (not white); (b) processed with color-destroying chemicals to discharge dye in discrete portions, then printed; (c) subjected to 2+ finishing operations.
"""

