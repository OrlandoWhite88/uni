# Chapter 98: Special Classification Provisions

CHAPTER_98_NOTES = """
CHAPTER 98 - SPECIAL CLASSIFICATION PROVISIONS

U.S. Notes:
1. Chapter 98 provisions are NOT subject to rule of relative specificity in GRI 3(a). Any article described in any provision in this chapter → classifiable in said provision IF conditions and requirements thereof and of any applicable regulations are met.

2. In absence of specific provision to contrary: tariff status of article is NOT affected by fact that it was previously imported into U.S. customs territory and cleared through customs, whether or not duty was paid on such previous importation.

3. Any article exempted under subchapters IV through VII, inclusive, or subchapter IX from duty → exempt also from any internal-revenue tax imposed upon or by reason of importation.

Statistical Notes:
1. Statistical data are NOT to be furnished for articles classified in headings of this chapter for which no statistical suffix is shown.

2. For articles with rate of duty derived from provision elsewhere in tariff schedule: citation shall be 10-digit statistical reporting number provided in this chapter followed by reporting number of provision from which rate is derived. Unit of quantity shall be same as for provision from which rate was derived.

SUBCHAPTER I - ARTICLES EXPORTED AND RETURNED:
The provisions in this subchapter (except subheadings 9801.00.70 and 9801.00.80) shall NOT apply to any article:
(a) Exported with benefit of drawback;
(b) Of a kind subject to internal-revenue tax at time of entry, UNLESS it was subject to such tax at time of exportation and tax was paid before exportation and not refunded;
(c) Manufactured or produced in U.S. customs bonded warehouse or under heading 9813.00.05 and exported under any provision of law.

For subheadings 9801.00.70 and 9801.00.80:
(a) When impracticable to establish whether drawback was allowed (or amount allowed) on returned article: assess duty equal to estimated drawback and internal-revenue tax allowable or refundable if imported merchandise used in manufacture of returned article were dutiable at rate applicable on date of entry, but not more than duty and tax if article were wholly of foreign origin;
(b) Tobacco products and cigarette papers and tubes may be released without payment of duty attributable to internal-revenue tax, for return to internal-revenue bond per IRC section 5704(d);
(c) Secretary of Treasury is authorized to ascertain and specify duty amounts equal to drawback or internal-revenue tax for articles or classes of articles, and to exempt articles for which collection involves expense and inconvenience disproportionate to probable duty amount.
"""

