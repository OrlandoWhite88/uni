# Chapter 64: Footwear, Gaiters and the Like; Parts of Such Articles

CHAPTER_64_NOTES = """
CHAPTER 64 - FOOTWEAR, GAITERS AND THE LIKE; PARTS OF SUCH ARTICLES

Notes:
1. This chapter does NOT cover:
   (a) Disposable foot or shoe coverings of flimsy material (e.g., paper, plastic sheeting) without applied soles → classify by constituent material;
   (b) Footwear of textile material without outer sole glued, sewn, or otherwise affixed to upper (section XI);
   (c) Worn footwear (heading 6309);
   (d) Articles of asbestos (heading 6812);
   (e) Orthopedic footwear or other orthopedic appliances, or parts (heading 9021);
   (f) Toy footwear or skating boots with ice or roller skates attached; shin-guards or similar protective sportswear (chapter 95).

2. "Parts" (heading 6406) does NOT include: pegs, protectors, eyelets, hooks, buckles, ornaments, braid, laces, pompons, or other trimmings (classify in appropriate headings), or buttons or other goods of heading 9606.

3. For this chapter:
   (a) "Rubber" and "plastics" include woven fabrics or other textile products with external layer of rubber or plastics visible to naked eye (disregard color change);
   (b) "Leather" = goods of headings 4107 and 4112-4114.

4. Subject to note 3:
   (a) UPPER MATERIAL = constituent material with greatest external surface area (disregard accessories or reinforcements: ankle patches, edging, ornamentation, buckles, tabs, eyelet stays, similar attachments);
   (b) OUTER SOLE MATERIAL = material with greatest surface area in contact with ground (disregard accessories or reinforcements: spikes, bars, nails, protectors, similar attachments).

Subheading Note:
"Sports footwear" (subheadings 6402.12, 6402.19, 6403.12, 6403.19, 6404.11) applies ONLY to:
(a) Footwear designed for sporting activity with, or provision for attachment of, spikes, sprigs, cleats, stops, clips, bars, or the like;
(b) Skating boots, ski-boots, cross-country ski footwear, snowboard boots, wrestling boots, boxing boots, cycling shoes.

Statistical Notes:
(a) "Work footwear" = specialized footwear for men or women with:
    - Outer soles of rubber or plastics; AND
    - Designed for use in occupations (agricultural, construction, industrial, public safety, transportation) not conducive to casual, dress, or lightweight footwear; AND
    - Special features to protect against workplace hazards (resistance to chemicals, compression, grease, oil, penetration, slippage, static-buildup).
    
    EXCLUDES: sports footwear, tennis shoes, basketball shoes, gym shoes, training shoes; footwear worn over other footwear; open toes or open heels; slip-on type or footwear without laces or combination of laces and hooks/other fasteners (except heading 6401 footwear).

(b) "Footwear for men" = American men's size 6 and larger for males; does NOT include footwear commonly worn by both sexes.

(c) "Footwear for women" = American women's size 4 and larger, whether for females or types commonly worn by both sexes.

(d) "House slippers":
    (i) Footwear with outer soles ≤3.5 mm thick of cellular rubber, non-grain leather, or textile material; OR
    (ii) Footwear with outer soles ≤2 mm thick of poly(vinyl chloride), whether or not backed.
"""

