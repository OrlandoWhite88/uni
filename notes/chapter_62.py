CHAPTER_62_NOTES = """
CHAPTER 62 - ARTICLES OF APPAREL AND CLOTHING ACCESSORIES, NOT KNITTED OR CROCHETED

Notes:
1. This chapter applies ONLY to made up articles of any textile fabric other than wadding, EXCLUDING knitted or crocheted articles (other than heading 6212).

2. This chapter does NOT cover:
   (a) Worn clothing or other worn articles (heading 6309);
   (b) Orthopedic appliances, surgical belts, trusses or the like (heading 9021).

3. "Suit" (headings 6203, 6204) = set of 2-3 garments made up in identical outer surface fabric comprising:
   - One suit coat or jacket (outer shell exclusive of sleeves consists of 4+ panels) to cover upper body, possibly with tailored waistcoat (front same fabric as other components, back same as lining); AND
   - One garment to cover lower body: trousers, breeches, shorts (not swimwear), skirt, or divided skirt (no braces or bibs).
   
   Requirements: same fabric construction, color, composition; same style; corresponding or compatible size. May have piping in different fabric.
   
   If multiple lower body components: constituent lower part = one pair of trousers, OR for women's/girls' suits the skirt or divided skirt; others considered separately.
   
   "Suit" includes (even if not meeting all conditions): morning dress; evening dress/tailcoat; dinner jacket suits.

   "Ensemble" (other than suits and headings 6207, 6208 articles) = set of several pieces in identical fabric, for retail sale, comprising:
   - One garment for upper body (except waistcoats as second upper garment); AND
   - One or two different garments for lower body: trousers, bib and brace overalls, breeches, shorts (not swimwear), skirt, divided skirt.
   
   Requirements: same fabric construction, style, color, composition; corresponding or compatible size. Does NOT apply to track suits or ski-suits (heading 6211).

4. Headings 6205 and 6206 do NOT cover: garments with pockets below waist, ribbed waistband or other bottom tightening. Heading 6205 does NOT cover sleeveless garments.

5. "Shirts" and "shirt-blouses" = garments for upper body with long or short sleeves and full or partial opening at neckline; may have collar.
   "Blouses" = loose-fitting garments for upper body, may be sleeveless, with or without neckline opening; may have collar.

6. "Babies' garments and clothing accessories" (heading 6209) = articles for young children of body height ≤86 cm. Articles classifiable in both heading 6209 and other chapter headings → heading 6209.

7. Garments classifiable in both heading 6210 and other chapter headings (excluding 6209) → heading 6210.

8. "Ski-suits" (heading 6211) = garments or sets identifiable by appearance and texture as intended principally for skiing:
   (a) "Ski overall" = one-piece garment for upper and lower body; may have sleeves, collar, pockets, footstraps;
   (b) "Ski ensemble" = 2-3 piece set for retail sale: one garment (anorak, windbreaker, or similar with zipper, possibly with waistcoat) + one pair of trousers (whether or not above waist), breeches, or bib and brace overall. May also be overall + padded sleeveless jacket.
   
   All components: same texture, style, composition (whether or not same color); corresponding or compatible size.

9. HANDKERCHIEFS vs SCARVES: square/approximately square scarves with no side >60 cm → handkerchiefs (heading 6213). Handkerchiefs with any side >60 cm → heading 6214.

10. GENDER CLASSIFICATION: left over right closure at front = men's or boys'; right over left closure at front = women's or girls'. Does not apply where garment cut clearly indicates one sex.

11. Garments not identifiable as men's/boys' or women's/girls' → classify as women's or girls'.

12. Articles of this chapter may be made of metal thread.

Statistical Notes:
1. Ensembles (subheadings 6203.21-6203.29, 6204.21-6204.29): report statistically as separate articles under most appropriate 10-digit number within same 8-digit subheading.

2. "Ski/snowboard pants" = ankle-length pants of synthetic fabrics (with or without insulation), with pockets (at least one with zippered or hook-and-loop closure), sealed seams at front and back rise, hidden leg sleeves with ankle tightening, and one or more of: side openings, scuff guards, seat reinforcement. "Sealed seam" = seam sealed by taping, gluing, bonding, cementing, fusing, or similar so air and water cannot pass through.
"""

